#!/usr/bin/env node
/**
 * Staging audit API probe — infra #50–#52 gate.
 *
 * Exits 0 when POST /audit/bundles is routed (not 404).
 * Writes durable evidence artifacts when OUTPUT_DIR is set.
 *
 * Usage:
 *   node 03-platform/scripts/staging-audit-probe.mjs
 *   OUTPUT_DIR=01-docs/audit node 03-platform/scripts/staging-audit-probe.mjs
 */
import { writeFileSync, mkdirSync } from 'node:fs';
import { execSync } from 'node:child_process';
import { resolve } from 'node:path';

const base = (process.env.GTCX_STAGING_AUDIT_URL ?? 'https://api.staging.gtcx.trade').replace(
  /\/$/,
  '',
);
const ua = process.env.GTCX_STAGING_UA ?? 'Mozilla/5.0 (GTCX staging-audit-probe)';
const outputDir = process.env.OUTPUT_DIR ?? null;

function gitSha() {
  try {
    return execSync('git rev-parse --short HEAD', { encoding: 'utf-8', stdio: ['pipe', 'pipe', 'ignore'] }).trim();
  } catch {
    return 'unknown';
  }
}

function gitBranch() {
  try {
    return execSync('git rev-parse --abbrev-ref HEAD', { encoding: 'utf-8', stdio: ['pipe', 'pipe', 'ignore'] }).trim();
  } catch {
    return 'unknown';
  }
}

async function probe(path, init = {}) {
  const url = `${base}${path}`;
  const start = Date.now();
  let status = '000';
  let error = null;
  try {
    const res = await fetch(url, {
      ...init,
      headers: {
        'User-Agent': ua,
        ...(init.headers ?? {}),
      },
    });
    status = res.status;
  } catch (err) {
    error = err.message;
  }
  return { url, path, status, error, latencyMs: Date.now() - start };
}

function isoDate(d = new Date()) {
  return d.toISOString().split('T')[0];
}

function isoTs(d = new Date()) {
  return d.toISOString();
}

async function main() {
  const sha = process.env.GITHUB_SHA ? process.env.GITHUB_SHA.slice(0, 8) : gitSha();
  const branch = process.env.GITHUB_REF_NAME ?? gitBranch();
  const now = new Date();

  const bundles = await probe('/audit/bundles', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: '{}',
  });
  const query = await probe('/audit/query', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: '{}',
  });

  const pass = bundles.status !== 404 && query.status !== 404;

  // Stdout logs (always)
  console.log(`POST ${base}/audit/bundles → ${bundles.status}${bundles.error ? ' (' + bundles.error + ')' : ''} (${bundles.latencyMs}ms)`);
  console.log(`POST ${base}/audit/query → ${query.status}${query.error ? ' (' + query.error + ')' : ''} (${query.latencyMs}ms)`);

  // Evidence artifacts (when OUTPUT_DIR is set)
  if (outputDir) {
    mkdirSync(outputDir, { recursive: true });

    const dateStr = isoDate(now);
    const tsStr = isoTs(now);

    const jsonArtifact = {
      schema: 'gtcx-staging-smoke/v1',
      generatedAt: tsStr,
      repo: 'gtcx-infrastructure',
      commit: sha,
      branch,
      target: base,
      probes: [bundles, query],
      pass,
      notes: pass
        ? 'Audit routes reachable (expect 400/405/503 until signed bundle + resolver)'
        : 'FAIL: audit routes still 404 (ingress or compliance-gateway image)',
    };

    const jsonPath = resolve(outputDir, `staging-smoke-evidence-${dateStr}.json`);
    writeFileSync(jsonPath, JSON.stringify(jsonArtifact, null, 2));
    console.log(`JSON evidence: ${jsonPath}`);

    const mdPath = resolve(outputDir, `staging-smoke-evidence-${dateStr}.md`);
    const md = `---
title: 'Staging Smoke Evidence — Audit API Reachability'
status: current
date: '${dateStr}'
owner: infrastructure-security-engineer
tier: critical
tags: ['audit', 'staging', 'smoke', 'evidence', 'prd-002']
review_cycle: on-change
infra_issue: 'https://github.com/gtcx-ecosystem/gtcx-infrastructure/issues/50'
---

# Staging Smoke Evidence — ${dateStr}

> Generated by \`03-platform/scripts/staging-audit-probe.mjs\` at ${tsStr}.
> Commit: \`${sha}\` · Branch: \`${branch}\`

## Target

\`\`\`text
${base}
\`\`\`

## Results

| Probe | Method | Status | Latency |
|-------|--------|--------|---------|
| \`${bundles.path}\` | POST | ${bundles.status}${bundles.error ? ' (' + bundles.error + ')' : ''} | ${bundles.latencyMs}ms |
| \`${query.path}\` | POST | ${query.status}${query.error ? ' (' + query.error + ')' : ''} | ${query.latencyMs}ms |

## Verdict

${pass ? '**PASS** — Audit routes are reachable (not 404).' : '**FAIL** — One or more audit routes returned 404.'}

${pass ? '> Note: Non-404 status (e.g., 400, 405, 503) is expected until signed bundles and TradePass DID resolver are fully wired.' : '> Next: verify ingress routing, compliance-gateway image tag, and pod health in staging.'}

## Cross-repo

- Unblocks: \`gtcx-intelligence\` bank-grade evidence gaps (staging deploy smoke + external validation)
- Related: \`gtcx-protocols\` PRD-002 Tier B DID resolver contract
- Evidence consumed by: \`gtcx-docs\` audit framework (master-audit prompt)

## Agent attestation

- [x] Staging routes probed from external egress
- [x] Evidence artifact generated
- [ ] Signed bundle acceptance test (blocked on TradePass resolver)
`;

    writeFileSync(mdPath, md);
    console.log(`Markdown evidence: ${mdPath}`);
  }

  if (!pass) {
    console.error('FAIL: audit routes still 404 (ingress or compliance-gateway image)');
    process.exit(1);
  }

  console.log('PASS: audit routes reachable (expect 400/405/503 until signed bundle + resolver)');
  process.exit(0);
}

main().catch((err) => {
  console.error('Unexpected error:', err);
  process.exit(1);
});
