import assert from 'node:assert';
import { describe, it } from 'node:test';
import { base58btcDecode, ed25519MultibaseToJwk } from '../../src/audit-bundles/multibase.mjs';

describe('base58btcDecode', () => {
  it('round-trips a known encoding from gtcx-protocols', () => {
    // Encoded by gtcx-protocols country-support-packages/_tools/multibase-ed25519.mjs
    const encoded = 'zTjsU3jY';
    const decoded = base58btcDecode(encoded.slice(1));
    assert.deepStrictEqual([...decoded], [0xed, 0x01, 0x01, 0x02, 0x03]);
  });

  it('decodes a real Ed25519 public key multibase', () => {
    const encoded = 'z6Mktp7jNeFWtpHw4qxgGk5E89LHbRo5DKUGvgPDReFgUoNW';
    const decoded = base58btcDecode(encoded.slice(1));
    // Should be 34 bytes: 2-byte multicodec prefix + 32-byte raw key
    assert.strictEqual(decoded.length, 34);
    assert.strictEqual(decoded[0], 0xed);
    assert.strictEqual(decoded[1], 0x01);
  });

  it('throws on invalid base58 characters', () => {
    assert.throws(() => base58btcDecode('0'), /Invalid base58 character/);
    assert.throws(() => base58btcDecode('O'), /Invalid base58 character/);
    assert.throws(() => base58btcDecode('l'), /Invalid base58 character/);
  });
});

describe('ed25519MultibaseToJwk', () => {
  it('converts a real multibase to Ed25519 JWK', () => {
    // Known test vector generated by gtcx-protocols
    const multibase = 'z6Mktp7jNeFWtpHw4qxgGk5E89LHbRo5DKUGvgPDReFgUoNW';
    const jwk = ed25519MultibaseToJwk(multibase);
    assert.strictEqual(jwk.kty, 'OKP');
    assert.strictEqual(jwk.crv, 'Ed25519');
    assert.strictEqual(typeof jwk.x, 'string');
    assert.ok(jwk.x.length > 0);
  });

  it('converts multibase without multicodec prefix (raw 32 bytes)', () => {
    // 32 zero bytes encoded as base58btc
    const raw32 = Buffer.alloc(32, 0);
    // base58btc of 32 zeros is "11111111111111111111111111111111" (32 ones)
    const multibase = 'z11111111111111111111111111111111';
    const jwk = ed25519MultibaseToJwk(multibase);
    assert.strictEqual(jwk.kty, 'OKP');
    assert.strictEqual(jwk.crv, 'Ed25519');
    assert.strictEqual(jwk.x, 'AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA');
  });

  it('throws on non-z prefix', () => {
    assert.throws(() => ed25519MultibaseToJwk('b123'), /must start with z/);
  });

  it('throws on too-short input', () => {
    assert.throws(() => ed25519MultibaseToJwk('z12'), /must be 32 bytes/);
  });
});
